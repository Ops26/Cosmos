    <h1>PSN_6 VIVIENDAS EN VERONA</h1>
      <p class="break-word, desc" style="font-size: 12px;">The origin of the project is developed from a series of program container boxes and terraces, sewn between four vertical platforms that delimit the space.
The house is confined within stone walls as an enclosure, where an intimate space is generated in which a universe of its own is built.
The feeling of horizontality is maintained through the horizontal bands of exposed white concrete, pieces that house the boxes where different rooms are located.
The setback of the volumes is replicated in the design of the pool, through a broken rectangle that recalls the horizontal set and the introduction of these elements.
Game of lines. The distribution of the boxes between the concrete planes generates intermediate spaces that sew the different volumes together and house the shared uses of the house. Interior spaces with exterior continuity, which present framed visuals of the garden that surrounds the house and allow the crossing of perspectives between different points.
Materials are displayed in their natural state through the use of wood, stone, and concrete. The vertical walls that generate the rhythm of the house are made of masonry stone, differentiating themselves from the rest of the elements of the house due to their materiality.
The horizontal lines are shown in the natural state of white concrete.
It is played with natural light, controlling its indirect entry into each of the rooms. The spaces generated between the volumes and the gaps in the slab create a contrast of light and shadow throughout the house.

</p>
