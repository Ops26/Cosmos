    <h1>CTL_VIVIENDA UNIFAMILIAR EN MIAMI</h1>
      <p class="break-word, desc" style="font-size: 12px;">This House is located in an exclusive sector of the city of Miami that has a series of channels, which provide independent access to the sea to each house in this area. These conditions make this sector standout for presenting homes with high quality standards and contemporary design, which consolidate an identity of prosperity and avant-garde. However, this particular home had an old aesthetic and spaces added at different times that made difficult to define a clear concept in its design and hinder the use of the spaces inside of it.
Consequently, a design was presented that seeks to define a simple volumetry but with strong orthogonal gestures that give the sensation of stability and strength as well as elegance. In addition, an open layout is generated in the social areas of the house to allow the access of light and give a feeling of spaciousness and abundance.
The finishes that were chosen for this proposal are located within a range of light colors to convey freshness, luminosity and spaciousness. Marbles and woods were chosen for floors and some walls that give warmth and elegance to the design.
The outdoor spaces, as expected, play a fundamental part in life in this type of housing and therefore, special interest was put on them. An infinity pool facing the sea and a series of terraces that could host the different activities that usually take place in these spaces was proposed.

</p>
