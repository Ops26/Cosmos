      <p class="break-word, desc" style="font-size: 12px;">The Project is located in an expansion zone in Barranquilla, where numerous social housing and infrastructure projects will be developed over the next few years to meet the future needs of an important population that will inhabit this area. This specific project consists of 400 housing units and common areas within the complex.
Given the limited budget for the project, the purchasing power of the people, their lifestyle and needs, it was a challenge to develop a very efficient design that would provide comfortable, sufficient spaces and at the same time dignity. Due to these reasons, a contemporary house concept was designed, with well-defined, orthogonal volumes and with some cantilevers, very different to the traditional gable roof scheme. On the other hand, the design proposes a narrow layout for the house, illuminated through internal patios, allowing better control of the visuals between neighbors and exceptional temperature regulation, especially in this region where high temperatures arise throughout the whole year.
Additionally, in order to be able to offer more area to each housing unit, we will implement a construction system in Tilt-Up. Although this system was more expensive at first, it saves time and rework.



