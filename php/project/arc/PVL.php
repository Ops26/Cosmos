The pavilion is divided into three parts: the clinic, the exhibition area and the sales area.
The materiality chosen for the exterior is white since this colour is used in architecture mainly for his three properties: purity, simplicity and sensitivity to light. For the interior, the combination of wood and white has been chosen for the sales and exhibition areas, providing a different warm atmosphere in the more public areas. While for the clinic area, which is the one with the largest surface, has the white elements to define the aesthetic of elegance and provide a high level of comfort inside.
In addition, the white curved volume of the project is also slightly raised above ground level, to give the building a certain prominence and monumentality from the flat surrounding.
The ground floor is all glazed so permeable in view and lighting, since we have located the public program and we were looking for a direct connection to the forest, while the first floor is perforated with a small continuous window. The project during night shows a subtle and continuous light line in the middle of the forest, it catches the human eye
