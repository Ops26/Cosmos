    <h1 class="title">COT_VIVIENDA UNIFAMILIAR EN EL RETIRO</h1>
      <p class="break-word, desc" style="font-size: 12px;">The region where this project is located stands out for preserving a very traditional aesthetic in its constructions. Moreover, our client wanted for his house to maintain the old traditions. Nevertheless, a contemporary influence was also added to the aesthetics. In addition, the project lot has a beautiful and abundant natural environment, which should be highlighted through the design.
Taking these conditions into account, we developed a design of a house with ample proportions, but with an austere aesthetic, typical of the region. The shape of the house is divided into two main volumes with gabled roofs. The first volume contains the social spaces and the second the private ones, both are linked by corridors that run along the house accompanied by interior patios that emphasize the value of the natural environment within the project, which allows permanent contact with vegetation. On the other hand, the visuals of all the rooms of the house are oriented towards a near forest and have a distant view of surrounding mountains.
Both, the orientation of the spaces and the distribution, as well as the volume of the house and the choice of materials, pay tribute not only to the construction tradition of the region, but also to its natural context. The warmth, austerity, simplicity of the materials and formal gestures of the house allow the natural environment and the life that takes place inside to always be the protagonists of the project.

</p>
