      <p class="break-word, desc" style="font-size: 12px;">The project consists in the design of a new production base which is going to have among other functions: offices, staff residences and factories buildings. Schemes design process consist in defining clearly the areas of production and the ones for support and management. The whole project stands on a podium that has several shared spaces for the workers and allows the direct connection between the buildings thanks to bridges and ramps. 
The facades are designed with metallic profiles that give a shading effect using different distances between them and defining the production volumes.
The facades without the metallic profiles uses the “light box” concept, made up with U-Glass; in this case, they correspond to the offices and managing areas. Under every “light cube” the architects subtracted a volume from the podium to make entrances. The new development is going to be a new landmark for the city of Guangdong and an icon for the company’s brand.





