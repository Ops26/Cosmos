    <h1>ECO_SALINAS HOTEL LAS SALINAS</h1>
      <p class="break-word, desc" style="font-size: 12px;">It is proposed to make an interspersed arrangement of cabins as cells inside of a system, of a community, leaving between them some interstitial spaces that act such as trails and isolation areas. These trails serve as spaces of connection and interaction and at the same time allow breathing space between each housing unit. It is possible to generate not only a certain independence, but also the flow of air, vegetation, and fauna of the place.

Each cabin works independently, containing private spaces inside for personal enjoyment and moments of relaxation. However, these open to the common spaces in certain strategic points to generate community and expand those outdoor enjoyment areas that are smaller than the interior of each one.

The main objective for the design of the cabins was to generate spaces that despite to have the possibility of being open to the outside to allow a feeling of amplitude, adequate lighting and ventilation and constant contact with the landscape and the vegetation, they could conserve moments of privacy, which are essential for a pleasant stay within this idea of urbanization.

This objective was achieved by designing a cabin typology circumscribed within a wall perimeter, within which a series of modules with different functions are located, among which
which patios are arranged that particularly serve these interior spaces. Basically, the cabin contains 4 modules, two for rooms and two for common spaces, living room and kitchen and dining room respectively.




</p>
