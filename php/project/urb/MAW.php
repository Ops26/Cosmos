    <h1>LAC_SANITARY COMPLEX IN LAC ROSE  </h1>
      <p class="break-word, desc" style="font-size: 12px;">The finalist design for a Residential high-rise of 260 apartments in one of the most important gates of Riyadh City in the KSA. The Mawten’s Residence will become the crossroad of Riyadh’s cultures and a business landmark for the entire city. The project represents a unique, modern and heritage respectful apartment complex in Riyadh city.
A design proposal that interprets the culture and ease of necessity in today’s daily life linked to the historical roots of the city, in line with Saudi Vision 2030.
One-of-a-kind apartment unit along with many other facilities (parking, swimming pool, gym, gardens, kids’ play areas, recreational areas, retail... etc.)
Mawten’s Residence aims to provide an enjoyable and private daily life from an exclusive point of view: the intersection of the K. Salam’s and K. Fahd Roundabout, one of the most important gates of the city.
With the purpose of thinking about a project not limited to the project itself, we want to stimulate its sphere of intervention. The new form is absorbing various complexities and transforming them as the new enriched identity, useful to regenerate and lead the growing city neighbourhood. The traditional shape and the greenery on the additional terraces that are rotating vertically until its top gives a majestic appearance that allows the new project to be recognizable as a landmark.




