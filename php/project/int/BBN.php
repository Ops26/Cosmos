    <h1>BBN_BONHO BY LA TORRE</h1>
      <p class="break-word, desc" style="font-size: 12px; ">Not all spaces feel like home, nor do they transmit that peace to you. Therefore, this collaboration seeks to ensure that these characteristics are met. In addition, making the most out of the surrounding environment is one of the goals.
The project idea is born from creating a cosy and warm environment where you breathe love and serenity. Where your clients can feel that they are living their own movie and they are the protagonists. Silence, peace, the feeling of space and freedom, fresh air and life in the middle of nature are the most remarkable and luxurious features of this property. Surrounded by greenery, away from the noise and stress of the city, a touch of simplicity and warmth has been given to the interior and exterior space, allowing event organizers to modify the same space with furniture. The place has wonderful outdoor and indoor facilities.
The design that Cosmos wanted for the architecture of the farmhouse was to give it a light touch where 4 fundamental pillars were united: minimalism, whiteness, a rural and timeless world. A place where nature can merge with the “rural life” while maintaining all the existing charm, where the assistant can evoke the time of our grandparents in the villages. Everything revolves around simplicity and clarity. Silence, rest, disconnection, seem assured. A farmhouse like this could only resort to local materials such as stone, white lime and wood. Everything refers to the natural, to the timeless.

It has been a pleasure for us to work on this type of project, since our goal has always been based on Álvaro Siza's phrase "tradition is a challenge for innovation".







